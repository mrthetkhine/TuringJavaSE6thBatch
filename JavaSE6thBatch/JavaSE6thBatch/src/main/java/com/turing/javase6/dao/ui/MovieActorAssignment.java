    void loadAllMovie()
    {
        List<Movie> movies = this.movieService.getAllMovie();
        for(Movie movie : movies)
        {
            this.movies.add( movie);
            this.cboMovie.addItem(movie.title());
        }
    }
    void loadAllActor()
    {
        List<Actor>actors = this.actorService.getAllActor();
        for(Actor actor : actors)
        {
            this.actors.add( actor);
            this.cboActor.addItem(actor.name());
        }
    }
