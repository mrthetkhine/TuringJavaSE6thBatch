    public void assignActorToMovie()
    {
        int movieIndex = this.cboMovie.getSelectedIndex();
        int actorIndex= this.cboActor.getSelectedIndex();
        
        Movie movie = this.movies.get(movieIndex);
        Actor actor = this.actors.get(actorIndex);
        
        System.out.println("Movie "+movie + " Actor "+actor);
        this.movieActorAssignmentService.addActorToMovie(movie.id(), actor.id());
        JOptionPane.showMessageDialog(this, "Actor have been successfully assigened to movie");
    }
